// ─── Lookup ───────────────────────────────────────────────────────────────────

export interface Opening {
  eco: string;
  name: string;
}

/**
 * Given the SAN move list for a game, return the ECO opening name
 * by finding the longest matching prefix in the database.
 */
export function detectOpening(moves: string[]): Opening | null {
  if (!moves.length) return null;
  const moveStr = moves.join(' ');
  for (const entry of OPENINGS) {
    if (moveStr.startsWith(entry.moves)) {
      return { eco: entry.eco, name: entry.name };
    }
  }
  return null;
}
